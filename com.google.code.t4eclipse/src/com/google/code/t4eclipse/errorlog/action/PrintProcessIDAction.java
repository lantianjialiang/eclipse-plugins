package com.google.code.t4eclipse.errorlog.action;

import java.lang.management.ManagementFactory;

import org.eclipse.jface.action.IAction;
import org.eclipse.jface.dialogs.MessageDialog;
import org.eclipse.jface.viewers.ISelection;
import org.eclipse.swt.widgets.Display;
import org.eclipse.ui.IWorkbenchWindow;
import org.eclipse.ui.IWorkbenchWindowActionDelegate;

public class PrintProcessIDAction implements IWorkbenchWindowActionDelegate {

	public void dispose() {
		// TODO Auto-generated method stub

	}

	public void init(IWorkbenchWindow window) {
		// TODO Auto-generated method stub

	}

	public void run(IAction action) {

		String processStr = ManagementFactory.getRuntimeMXBean().getName();

		String processSTR_ALL = "The Current Eclipse/RCP Process:"+processStr;
		MessageDialog.openInformation(Display.getCurrent().getActiveShell(),
				"", processSTR_ALL);
	}

	public void selectionChanged(IAction action, ISelection selection) {
		// TODO Auto-generated method stub

	}

}
